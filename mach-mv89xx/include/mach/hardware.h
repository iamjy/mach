#ifndef __ASM_ARCH_HARDWARE_H
#define __ASM_ARCH_HARDWARE_H

#include <asm/sizes.h>

/* macro to get at IO space when running virtually */
#define IO_ADDRESS(x)		(0xF0000000 + (((x) >> 4) & 0x0FF00000) \
			+ ((x) & 0x000FFFFF))
#define __io_address(n)		((void __iomem *)IO_ADDRESS(n))

#endif
